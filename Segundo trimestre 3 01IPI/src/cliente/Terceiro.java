package cliente;

/**
 *
 * @author Aluno
 */
public class Terceiro {
    private String nome;
    private float id;
    private int quantidadeDePedidos;
    private String primeiroPedido;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public float getId() {
        return id;
    }

    public void setId(float id) {
        this.id = id;
    }

    public int getQuantidadeDePedidos() {
        return quantidadeDePedidos;
    }

    public void setQuantidadeDePedidos(int quantidadeDePedidos) {
        this.quantidadeDePedidos = quantidadeDePedidos;
    }

    public String getPrimeiroPedido() {
        return primeiroPedido;
    }

    public void setPrimeiroPedido(String primeiroPedido) {
        this.primeiroPedido = primeiroPedido;
    }
}
